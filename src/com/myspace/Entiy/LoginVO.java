package com.myspace.Entiy;


public class LoginVO {

	private String log_ip;
	private String log_city;
	private String log_time;
	private long log_count;
	private String log_url;
	public String getLog_ip() {
		return log_ip;
	}
	public void setLog_ip(String log_ip) {
		this.log_ip = log_ip;
	}
	public String getLog_city() {
		return log_city;
	}
	public void setLog_city(String log_city) {
		this.log_city = log_city;
	}
	
	public String getLog_time() {
		return log_time;
	}
	public void setLog_time(String log_time) {
		this.log_time = log_time;
	}
	public long getLog_count() {
		return log_count;
	}
	public void setLog_count(long log_count) {
		this.log_count = log_count;
	}
	public String getLog_url() {
		return log_url;
	}
	public void setLog_url(String log_url) {
		this.log_url = log_url;
	}
	
}
